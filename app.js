function switchTab(name) {
  currentTab = name;
  document.querySelectorAll(".tab").forEach(t => {
    t.setAttribute("aria-selected", String(t.dataset.tab === name));
  });
  $("#tab-now").hidden = name !== "now";
  $("#tab-plans").hidden = name !== "plans";
  $("#tab-principles").hidden = name !== "principles";
  if (name === "now") renderNow();
  else if (name === "plans") renderPlans();
  else if (name === "principles") renderPrinciples();
}

// ============================================================
//  Settings panel
// ============================================================

function openSettings() {
  // Fill the form from current settings each time it opens.
  $("#race-date").value = settings.raceDate || "";
  $("#weekly-volume").value = settings.weeklyVolumeKm ?? "";
  $("#block-weeks").value = String(settings.blockWeeks || 18);
  $("#settings-panel").classList.add("open");
  $("#settings-panel").setAttribute("aria-hidden", "false");
  $("#panel-backdrop").classList.add("open");
}

function closeSettings() {
  $("#settings-panel").classList.remove("open");
  $("#settings-panel").setAttribute("aria-hidden", "true");
  $("#panel-backdrop").classList.remove("open");
}

// Read the form, save, and re-render. Autosave on every change (no save button).
function applySettingsFromForm() {
  const raceDate = $("#race-date").value || "";
  const volRaw = $("#weekly-volume").value;
  const vol = volRaw === "" ? null : Math.max(0, Math.round(Number(volRaw)));
  const blockWeeks = Number($("#block-weeks").value) || 18;

  settings = {
    ...settings,
    raceDate,
    weeklyVolumeKm: Number.isFinite(vol) ? vol : null,
    blockWeeks
  };
  const ok = saveSettings(settings);
  if (!ok) {
    toast("Could not save to this browser. Your change may not be kept.", true);
  }
  if (currentTab === "now") renderNow();
}

// ============================================================
//  Export / Import
// ============================================================

function doExport() {
  if (!hasSettings()) {
    toast("Set your race date first, then export.");
    return;
  }
  exportToFile(settings);
  renderBackupStatus();
  toast("Settings exported.");
}

function doImport(file) {
  const reader = new FileReader();
  reader.onload = () => {
    try {
      const imported = parseImport(String(reader.result));
      settings = imported;
      const ok = saveSettings(settings);
      if (!ok) { toast("Imported, but could not save to this browser.", true); }
      else { toast("Settings imported."); }
      switchTab("now");
    } catch (e) {
      toast(e.message || "Could not read that file.", true);
    }
  };
  reader.onerror = () => toast("Could not read that file.", true);
  reader.readAsText(file);
}

// ============================================================
//  Wiring
// ============================================================

function wire() {
  // Tabs
  document.querySelectorAll(".tab").forEach(t => {
    t.addEventListener("click", () => switchTab(t.dataset.tab));
  });

  // Settings panel
  $("#settings-done").addEventListener("click", closeSettings);
  $("#panel-backdrop").addEventListener("click", closeSettings);
  $("#settings-form").addEventListener("change", applySettingsFromForm);
  $("#weekly-volume").addEventListener("input", applySettingsFromForm);

  // About modal
  $("#about-btn").addEventListener("click", () => { $("#about-modal").hidden = false; });
  $("#about-close").addEventListener("click", () => { $("#about-modal").hidden = true; });
  $("#about-modal").addEventListener("click", e => {
    if (e.target === $("#about-modal")) $("#about-modal").hidden = true;
  });

  // Export / Import
  $("#export-btn").addEventListener("click", doExport);
  $("#import-btn").addEventListener("click", () => $("#import-file").click());
  $("#import-file").addEventListener("change", e => {
    const file = e.target.files && e.target.files[0];
    if (file) doImport(file);
    e.target.value = "";   // allow re-importing the same file
  });

  // Close panels / modal on Escape
  document.addEventListener("keydown", e => {
    if (e.key === "Escape") {
      closeSettings();
      $("#about-modal").hidden = true;
    }
  });
}

// ---- Boot ----
renderBackupStatus();
wire();
switchTab("now");
