function renderNow() {
  const host = $("#tab-now");

  if (!hasSettings()) {
    host.innerHTML = `
      <div class="empty">
        <h2>Set your race date</h2>
        <p>Enter your marathon date and, if you like, your current weekly volume. This tool then shows where you roughly are and how the major plans would treat this point.</p>
        <button class="btn primary" id="empty-open-settings">Set your race date</button>
      </div>`;
    $("#empty-open-settings").addEventListener("click", openSettings);
    return;
  }

  const race = parseISODate(settings.raceDate);
  const cmp = getWeekComparison(settings.raceDate, settings.weeklyVolumeKm, settings.blockWeeks);
  const wtr = cmp.weeksToRace;

  // ---- Countdown headline ----
  let headline, sub;
  if (cmp.overall === "done") {
    headline = "Race day has passed";
    sub = `Your race date was ${formatDate(race)}. Update it in Settings when the next one is set.`;
  } else if (cmp.overall === "raceweek") {
    headline = "Race week";
    sub = `Race day is ${formatDate(race)}.`;
  } else {
    headline = `<span class="countdown-num">${wtr}</span> ${weeksWord(wtr)} until race day`;
    sub = `Race day is ${formatDate(race)}.`;
  }

  let html = `
    <div class="card">
      <p class="countdown">${headline}</p>
      <p class="countdown-sub">${esc(sub)}</p>
      ${cmp.overall !== "done" ? timelineSVG(cmp, race) : ""}
    </div>`;

  // ---- Current state ----
  if (cmp.overall === "before") {
    html += basePhaseCard(cmp, race);
    html += beforePlansCard(cmp);
  } else if (cmp.overall === "active" || cmp.overall === "raceweek") {
    html += activeComparisonCard(cmp);
    html += yourPositionCard(cmp);
  } else if (cmp.overall === "done") {
    html += `<div class="card"><p class="lead">Set a new race date to see the comparison again.</p></div>`;
  }

  // ---- Disclaimer (always, when settings exist) ----
  html += `<p class="disclaimer">Approximate shapes, not the real schedules. The numbers are generated by this tool and snapped to the nearest 5 km. See each plan's source for the actual schedule.</p>`;

  // ---- Edit settings ----
  html += `<div style="margin-top:1rem"><button class="btn" id="now-open-settings">Edit settings</button></div>`;

  host.innerHTML = html;
  $("#now-open-settings").addEventListener("click", openSettings);
}

// Base-phase card: one recurring state, warm and not-a-beginner. No weekly cards.
function basePhaseCard(cmp, race) {
  const userStart = blockStartDate(race, cmp.userBlockWeeks);
  const startsIn = cmp.userBlockStartsInWeeks;
  let startLine;
  if (startsIn > 0) {
    startLine = `Your ${cmp.userBlockWeeks} week block would begin in about ${startsIn} ${weeksWord(startsIn)}, around ${formatDateShort(userStart)}.`;
  } else {
    startLine = `Your ${cmp.userBlockWeeks} week block has begun. The published 18 week plans start a little later.`;
  }
  return `
    <div class="card">
      <p class="section-h">Right now</p>
      <p class="lead" style="color:var(--ink)">You are outside the structured block. Keep consistent easy running with one long run a week, and stay healthy. There is no rush to ramp up yet.</p>
      <p class="plan-detail" style="margin-top:0.7rem">${esc(startLine)}</p>
    </div>`;
}

// In base phase, plans have not started. Show a compact "begins in" row each,
// with the one-line philosophy. Not weekly cards, just where each plan sits.
function beforePlansCard(cmp) {
  const rows = cmp.plans.map(r => {
    const inWks = r.startsInWeeks;
    return `
      <div class="plan-card">
        <div class="plan-card-head">
          <span class="plan-name">${esc(r.name)}</span>
          <span class="tier-badge">${esc(TIER_LABEL[r.tier] || r.tier)}</span>
        </div>
        <p class="plan-before"><span class="starts">Begins in about ${inWks} ${weeksWord(inWks)}</span></p>
        <p class="plan-detail">${esc(r.philosophy)}</p>
        <p class="plan-source"><a href="${esc(r.source.url)}" target="_blank" rel="noopener noreferrer">${esc(r.source.label)}</a></p>
      </div>`;
  }).join("");
  return `
    <div class="card">
      <p class="section-h">The plans, not started yet</p>
      <div class="plan-grid">${rows}</div>
    </div>`;
}

// Active state: a card per plan at this point in time.
function activeComparisonCard(cmp) {
  const active = cmp.plans.filter(r => r.state === "active");
  const rows = active.map(r => `
    <div class="plan-card">
      <div class="plan-card-head">
        <span class="plan-name">${esc(r.name)}</span>
        <span class="tier-badge">${esc(TIER_LABEL[r.tier] || r.tier)}</span>
      </div>
      <p class="plan-phase">${esc(r.phase)} &middot; ${esc(r.weekLabel)}</p>
      <p class="plan-km">~${r.approxKm} km<small> per week</small></p>
      <p class="plan-detail"><strong>Signature:</strong> ${esc(r.signatureWorkout)}</p>
      <p class="plan-meta">Guided by ${esc(paceApproachLabel(r.paceApproach))}</p>
      <p class="plan-source"><a href="${esc(r.source.url)}" target="_blank" rel="noopener noreferrer">${esc(r.source.label)}</a></p>
    </div>`).join("");
  return `
    <div class="card">
      <p class="section-h">Where each plan sits right now</p>
      <div class="plan-grid active-grid">${rows}</div>
    </div>`;
}

function paceApproachLabel(a) {
  return { mileage: "weekly mileage", pace: "pace targets", effort: "effort" }[a] || a;
}

// Your position: user's volume against the active spread, with an SVG ladder.
function yourPositionCard(cmp) {
  const pos = cmp.myPosition;
  if (!settings.weeklyVolumeKm) {
    return `
      <div class="card">
        <p class="section-h">Your position</p>
        <p class="lead">Add your current weekly volume in Settings to mark where you sit against the plans.</p>
      </div>`;
  }
  if (!pos) {
    return `
      <div class="card">
        <p class="section-h">Your position</p>
        <p class="lead">No plan is active at this point, so there is nothing to compare against yet.</p>
      </div>`;
  }
  const my = pos.myKm;
  let line;
  if (pos.above.length && pos.below.length) {
    line = `At about <span class="hi">${my} km</span> per week you are at or above ${listNames(pos.above)}, and below ${listNames(pos.below)}.`;
  } else if (pos.above.length && !pos.below.length) {
    line = `At about <span class="hi">${my} km</span> per week you are at or above every plan's mark for this point.`;
  } else {
    line = `At about <span class="hi">${my} km</span> per week you are below every plan's mark for this point.`;
  }
  return `
    <div class="card">
      <p class="section-h">Your position</p>
      <p class="position-line">${line}</p>
      ${ladderSVG(cmp)}
    </div>`;
}

function listNames(names) {
  if (names.length === 1) return esc(names[0]);
  if (names.length === 2) return `${esc(names[0])} and ${esc(names[1])}`;
  return names.slice(0, -1).map(esc).join(", ") + ", and " + esc(names[names.length - 1]);
}

// ---- Timeline: an SVG bar (now to race, block region shaded) plus an HTML
// legend below. The labels live in HTML, not SVG text, so they stay legible at
// mobile width where a wide viewBox would shrink SVG text too far to read. ----
function timelineSVG(cmp, race) {
  const W = 680, H = 26, padX = 12;
  const innerW = W - padX * 2;
  const wtr = Math.max(cmp.weeksToRace, 0);
  const trackY = 13;

  // Block region is the last `userBlockWeeks` before the race. Show a distinct
  // block-start marker only while the block is still ahead; once it is underway
  // (block longer than the time remaining) the whole track is the block.
  const span = Math.max(wtr, 1);
  const blockAhead = cmp.userBlockWeeks < span;
  const blockStartWeeksOut = Math.min(cmp.userBlockWeeks, span);
  const blockX = padX + innerW * (1 - blockStartWeeksOut / span);

  const raceStr = formatDateShort(race);
  const blockStr = formatDateShort(blockStartDate(race, cmp.userBlockWeeks));

  const blockDot = blockAhead
    ? `<circle cx="${blockX.toFixed(1)}" cy="${trackY}" r="5" fill="var(--sage-bright-deep)"/>`
    : "";

  const svg = `
    <svg class="timeline" viewBox="0 0 ${W} ${H}" preserveAspectRatio="none" role="img"
         aria-label="Timeline from now to race day, with your training block shaded.">
      <line x1="${padX}" y1="${trackY}" x2="${W - padX}" y2="${trackY}"
            stroke="var(--track)" stroke-width="6" stroke-linecap="round"/>
      <line x1="${blockX.toFixed(1)}" y1="${trackY}" x2="${W - padX}" y2="${trackY}"
            stroke="var(--sage-tint)" stroke-width="6" stroke-linecap="round"/>
      <circle cx="${padX}" cy="${trackY}" r="6" fill="var(--sage)"/>
      ${blockDot}
      <circle cx="${W - padX}" cy="${trackY}" r="6" fill="var(--sage-deep)"/>
    </svg>`;

  const blockLegend = blockAhead
    ? `<span><span class="dot" style="background:var(--sage-bright-deep)"></span>Block start, ${esc(blockStr)}</span>`
    : `<span><span class="dot" style="background:var(--sage-bright-deep)"></span>Your block is underway</span>`;

  return `${svg}
    <div class="timeline-legend">
      <span><span class="dot" style="background:var(--sage)"></span>Now</span>
      ${blockLegend}
      <span><span class="dot" style="background:var(--sage-deep)"></span>Race day, ${esc(raceStr)}</span>
    </div>`;
}

// ---- Volume bars: one horizontal bar per plan plus a "You" bar, sorted low to
// high, so the spread and where you sit in it read at a glance. Plain HTML and
// CSS bars (the design system's progress-track idiom) stay legible at any width,
// unlike SVG text in a wide viewBox. ----
function ladderSVG(cmp) {
  const active = cmp.plans.filter(r => r.state === "active" && r.approxKm);
  if (!active.length) return "";
  const my = cmp.myPosition.myKm;

  // Build the rows, insert "You", and sort ascending so you sit in the spread.
  const rows = active.map(r => ({ label: r.short || r.name, km: r.approxKm, you: false }));
  rows.push({ label: "You", km: my, you: true });
  rows.sort((a, b) => a.km - b.km || (a.you ? 1 : -1));

  const maxKm = Math.max(...rows.map(r => r.km), 1);
  const bars = rows.map(r => {
    const pct = Math.round((r.km / maxKm) * 100);
    return `
      <div class="vol-row${r.you ? " you" : ""}">
        <span class="vol-name">${esc(r.label)}</span>
        <span class="vol-track"><span class="vol-fill" style="width:${pct}%"></span></span>
        <span class="vol-val">~${r.km} km</span>
      </div>`;
  }).join("");

  return `<div class="vol-bars" role="img"
      aria-label="Your weekly volume compared with each plan's approximate volume for this point.">${bars}</div>`;
}

