const $ = sel => document.querySelector(sel);

// Escape any text that comes from data before putting it in innerHTML.
function esc(s) {
  return String(s)
    .replace(/&/g, "&amp;").replace(/</g, "&lt;").replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;");
}

const MONTHS = ["January", "February", "March", "April", "May", "June",
  "July", "August", "September", "October", "November", "December"];
const WEEKDAYS = ["Sunday", "Monday", "Tuesday", "Wednesday",
  "Thursday", "Friday", "Saturday"];

// English date, e.g. "Sunday, 9 May 2027". No locale dependence.
function formatDate(dateObj) {
  return `${WEEKDAYS[dateObj.getDay()]}, ${dateObj.getDate()} ${MONTHS[dateObj.getMonth()]} ${dateObj.getFullYear()}`;
}
// Shorter form, e.g. "9 May 2027".
function formatDateShort(dateObj) {
  return `${dateObj.getDate()} ${MONTHS[dateObj.getMonth()]} ${dateObj.getFullYear()}`;
}
function weeksWord(n) { return n === 1 ? "week" : "weeks"; }

const TIER_LABEL = { beginner: "Beginner", intermediate: "Intermediate", advanced: "Advanced" };

